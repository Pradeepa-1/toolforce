// Get random quote by category
function getQuote(category) {
  const key = category.toLowerCase().trim();
  const list = quotes[key] || quotes.motivation;
  return list[Math.floor(Math.random() * list.length)];
}

// Get all categories
function getCategories() {
  return Object.keys(quotes);
}

// Get random quote from any category
function getRandomQuote() {
  const cats = getCategories();
  const cat = cats[Math.floor(Math.random() * cats.length)];
  const q = getQuote(cat);
  return { ...q, category: cat };
}

// Get daily quotes (20 unique)
function getDailyQuotes(count = 20) {
  const all = [];
  Object.entries(quotes).forEach(([cat, list]) => {
    list.forEach(q => all.push({ ...q, category: cat }));
  });
  const shuffled = all.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

module.exports = { getQuote, getCategories, getRandomQuote, getDailyQuotes };
